const regex = {
    email:
      /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
    password:
      /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*?&])[A-Za-z\d@$!%*?&]{8,}$/,
    phone: /^[0-9]{10}$/,
    name: /^[a-zA-Z ]{2,30}$/,
    address: /^[a-zA-Z0-9\s,'-]{2,100}$/,
    city: /^[a-zA-Z ]{2,30}$/,
    state: /^[a-zA-Z ]{2,30}$/,
    zip: /^[0-9]{5}$/,
    otp: /^[0-9]{4}$/,
    numeric_value: /^[+]?\d*(?:[-\s]?\d*){10,}$/,
  };
  export default regex;